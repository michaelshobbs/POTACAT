#!/usr/bin/env node
// scripts/update-website-links.js
//
// Rewrites the download section in potacat-website/index.html to point at a
// new release. Idempotent — run it with the target version and the HTML file
// path, and it replaces everything between the DOWNLOAD-START / DOWNLOAD-END
// markers with a freshly generated block.
//
// Usage: node update-website-links.js <version> <path-to-index.html>

const fs = require('fs');
const path = require('path');

const version = process.argv[2];
const htmlPath = process.argv[3];

if (!version || !htmlPath) {
  console.error('Usage: node update-website-links.js <version> <path-to-index.html>');
  process.exit(1);
}

const REPO = 'https://github.com/Waffleslop/POTACAT';

function assetUrl(name) {
  return `${REPO}/releases/download/v${version}/${name}`;
}

// Keep in sync with electron-builder's artifact names in package.json build config
const urls = {
  winInstaller: assetUrl(`POTACAT-Setup-${version}.exe`),
  winPortable:  assetUrl(`POTACAT-Portable-${version}.exe`),
  macDmgX64:    assetUrl(`POTACAT-${version}.dmg`),
  macDmgArm64:  assetUrl(`POTACAT-${version}-arm64.dmg`),
  // Linux x86_64 (Intel/AMD) — the unsuffixed AppImage IS the x86_64 build
  // (electron-builder only adds an arch suffix for non-x64). Verified against
  // the v1.8.13 release assets.
  linuxAppImage: assetUrl(`POTACAT-${version}.AppImage`),
  linuxDeb:      assetUrl(`potacat_${version}_amd64.deb`),
  linuxRpm:      assetUrl(`potacat-${version}.x86_64.rpm`),
  // Linux arm64 / aarch64 — Raspberry Pi 4/5, sBitx, other ARM SBCs (issue #43).
  linuxAppImageArm64: assetUrl(`POTACAT-${version}-arm64.AppImage`),
  linuxDebArm64:      assetUrl(`potacat_${version}_arm64.deb`),
  linuxRpmArm64:      assetUrl(`potacat-${version}.aarch64.rpm`),
  releaseNotes:  `${REPO}/releases/tag/v${version}`,
};

// Download-click analytics hook. The `os` arg (windows|mac|linux) is passed
// through to /welcome.html so the page can show the right first-launch tip.
const dlClick = (os) => `onclick="dataLayer && dataLayer.push({'event':'download_click','download_version':'${version}','download_os':'${os}'}); setTimeout(function(){ window.location.href='/welcome.html?os=${os}'; }, 1000);"`;

const block = `<!-- DOWNLOAD-START — auto-generated by scripts/update-website-links.js -->
<div class="download-section" id="download">
  <div class="download-heading">
    <h2>Ready to Chase Some Parks?</h2>
    <p>Download POTACAT and start hunting POTA, SOTA, DX Cluster, and RBN spots.</p>
  </div>

  <div class="download-grid">
    <!-- Windows -->
    <div class="dl-card dl-card--primary">
      <div class="dl-card-icon">&#x1FA9F;</div>
      <div class="dl-card-label">Windows</div>
      <a href="${urls.winInstaller}" class="dl-btn" ${dlClick('windows')}>Download v${version}</a>
      <div class="dl-sub">64-bit installer &middot; Windows 10+</div>
      <div class="dl-sub"><a href="${urls.winPortable}" style="color: var(--text-muted);">Portable version</a></div>
    </div>

    <!-- Mac -->
    <div class="dl-card">
      <div class="dl-card-icon">&#x1F34E;</div>
      <div class="dl-card-label">Mac</div>
      <a href="${urls.macDmgArm64}" class="dl-btn" ${dlClick('mac')}>Apple Silicon v${version}</a>
      <div class="dl-sub">macOS 11+ &middot; <a href="/welcome.html?os=mac#welcome-tip" style="color: var(--text-muted);">First-launch tip &rarr;</a></div>
      <div class="dl-sub"><a href="${urls.macDmgX64}" style="color: var(--text-muted);" ${dlClick('mac')}>Intel Mac (x64)</a></div>
    </div>

    <!-- Linux -->
    <div class="dl-card">
      <div class="dl-card-icon">&#x1F427;</div>
      <div class="dl-card-label">Linux</div>
      <a href="${urls.linuxAppImage}" class="dl-btn" ${dlClick('linux')}>AppImage (x86_64) v${version}</a>
      <div class="dl-sub">Intel / AMD 64-bit &middot; <a href="${urls.linuxDeb}" style="color: var(--text-muted);">.deb</a> &middot; <a href="${urls.linuxRpm}" style="color: var(--text-muted);">.rpm</a></div>
      <div class="dl-sub" style="margin-top:8px;"><a href="${urls.linuxAppImageArm64}" style="color: var(--text-muted);" ${dlClick('linux')}>AppImage (ARM64)</a> &middot; <a href="${urls.linuxDebArm64}" style="color: var(--text-muted);">.deb</a> &middot; <a href="${urls.linuxRpmArm64}" style="color: var(--text-muted);">.rpm</a></div>
      <div class="dl-sub">ARM64 / aarch64 &middot; Raspberry Pi 4/5, SBCs</div>
    </div>
  </div>

  <div class="download-footer">
    Free &amp; open source &middot;
    <a href="${urls.releaseNotes}" target="_blank" rel="noopener">Release notes</a> &middot;
    <a href="#build-from-source">Build from source</a>
  </div>

  <details id="build-from-source" style="margin-top:32px;max-width:720px;margin-left:auto;margin-right:auto;padding:0 16px;">
    <summary style="cursor:pointer;color:var(--text-muted);font-size:0.9em;">Build from source</summary>
    <div style="margin-top:12px;font-size:0.9em;color:var(--text-muted);line-height:1.6;">
      <p>POTACAT is open source. If you'd rather build it yourself, clone the repo and run one of the <code>dist:*</code> scripts.</p>
      <p><strong>Requires <a href="https://git-scm.com" target="_blank" rel="noopener">Git</a> and <a href="https://nodejs.org" target="_blank" rel="noopener">Node.js 22+</a>.</strong></p>
      <p><em>Linux only:</em> install build deps first: <code>sudo apt install build-essential python3 libudev-dev cmake libhamlib-utils</code></p>
      <pre style="background:var(--bg-secondary,#0f1419);padding:12px;border-radius:4px;overflow-x:auto;"><span class="prompt-char">$</span> git clone https://github.com/Waffleslop/POTACAT.git
<span class="prompt-char">$</span> cd POTACAT
<span class="prompt-char">$</span> npm install
<span class="prompt-char">$</span> npm run dist:win     <span style="color:#888;"># or dist:mac / dist:linux</span></pre>
      <p>Installer appears in the <code>dist/</code> folder.</p>
    </div>
  </details>
</div>
<!-- DOWNLOAD-END -->`;

const html = fs.readFileSync(htmlPath, 'utf-8');
const startRe = /<!-- DOWNLOAD-START[\s\S]*?<!-- DOWNLOAD-END -->/;

let updated;
if (startRe.test(html)) {
  updated = html.replace(startRe, block);
} else {
  // First-time bootstrap: find the existing static download section and replace it.
  // We match the <!-- ===== DOWNLOAD ===== --> marker and the closing </div> of the
  // download-section div. If that marker isn't present either, bail out.
  const bootstrap = /<!--\s*=====?\s*DOWNLOAD\s*=====?\s*-->[\s\S]*?<div class="download-section"[\s\S]*?\n<\/div>/;
  if (!bootstrap.test(html)) {
    console.error('Could not find download section in', htmlPath);
    console.error('Expected either the auto-gen markers or the "===== DOWNLOAD =====" comment.');
    process.exit(1);
  }
  updated = html.replace(bootstrap, block);
}

if (updated === html) {
  console.log('No changes needed — already at v' + version);
  process.exit(0);
}

fs.writeFileSync(htmlPath, updated, 'utf-8');
console.log('Updated', htmlPath, 'to v' + version);
